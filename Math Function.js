let num = 10.7
console.log(Math.floor(num))//round the value down
console.log(Math.ceil(num))//round the value up
console.log(Math.round(num)) //round the value
console.log(num.toFixed(2)) //round the value to 2 decimal places
console.log(isNaN(num)) //check if the value is not a number
console.log(Math.random()) //generate a random number between 0 and 1
console.log(Math.max(5,7,1,8)) //returns the maximum value among the given numbers
console.log(Math.min(5,7,1,8)) //returns the minimum value among the given numbers
console.log(Math.pow(2,3)) //returns the value of 2 raised to the power of 3