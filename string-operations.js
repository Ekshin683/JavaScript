let str = "Hello"
console.log(str.toUpperCase()) //convert the string to uppercase
console.log(str.toLowerCase()) //convert the string to lowercase
console.log(str.indexOf("l")) //returns the index of the first occurrence of "l"
console.log(str.lastIndexOf("l")) //returns the index of the last occurrence of "l"
console.log(str.includes("l"))//checks if the string contains "l" or not
console.log(str.startsWith("H"))//checks if the string starts with "H" or not
console.log(str.endsWith("o"))//checks if the string ends with "o" or not
console.log(str.slice(1,3))//returns a new string from index 1 to 2
console.log(str.substring(1,3))//returns a new string from index 1 to 2
